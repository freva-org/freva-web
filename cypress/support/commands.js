Cypress.Commands.add('login', (session_id) => {
    cy.session([session_id], () => {
        cy.visit("localhost:8000/login")
        cy.get("#username").type("janedoe");
        cy.get("#password").type("janedoe123");
        cy.get("#kc-login").click();
    }, 
    {
        cacheAcrossSpecs: true
    })
})